const audio = new Audio()
let currentTrack = 0
let isPlaying = false
let volume = 0.5
let queue = []
let listeners = []
function notify() {
  const track = playlist[currentTrack]
  const progress = track ? audio.currentTime / track.duration : 0
  const data = {
    currentTrack,
    isPlaying,
    volume,
    progress: Math.min(progress, 1),
    track,
    queue,
    currentTime: audio.currentTime,
  }
  listeners.forEach(fn => fn(data))
}

function playSrc() {
  audio.play().catch(() => {
    if (audio.readyState >= HTMLMediaElement.HAVE_CURRENT_DATA) {
      audio.play().catch(() => {})
    } else {
      audio.addEventListener('canplay', () => audio.play().catch(() => {}), { once: true })
    }
  })
}

function setTrack(index) {
  if (index < 0 || index >= playlist.length) return
  currentTrack = index
  const track = playlist[index]
  audio.src = track.src
  audio.currentTime = 0
}

export function togglePlay() {
  if (audio.paused) {
    isPlaying = true
    if (!audio.src) setTrack(currentTrack)
    audio.play().catch(() => {
      if (audio.readyState >= HTMLMediaElement.HAVE_CURRENT_DATA) {
        audio.play().catch(() => {})
      } else {
        audio.addEventListener('canplay', () => audio.play().catch(() => {}), { once: true })
      }
    })
  } else {
    audio.pause()
    isPlaying = false
  }
  notify()
}

export function playTrackWithQueue(index) {
  if (index < 0 || index >= playlist.length) return
  queue = []
  for (let i = index + 1; i < playlist.length; i++) {
    queue.push(i)
  }
  isPlaying = true
  setTrack(index)
  playSrc()
  notify()
}

export function nextTrack() {
  if (queue.length > 0) {
    const next = queue.shift()
    isPlaying = true
    setTrack(next)
    playSrc()
    notify()
    return
  }
  if (currentTrack + 1 < playlist.length) {
    isPlaying = true
    setTrack(currentTrack + 1)
    playSrc()
    notify()
  }
}

export function prevTrack() {
  if (audio.currentTime > 3) {
    audio.currentTime = 0
    notify()
    return
  }
  if (currentTrack - 1 >= 0) {
    isPlaying = true
    setTrack(currentTrack - 1)
    playSrc()
    notify()
  }
}

export function seekTo(progress) {
  const track = playlist[currentTrack]
  if (!track) return
  audio.currentTime = progress * track.duration
  notify()
}

export function setVolume(v) {
  volume = Math.max(0, Math.min(1, v))
  audio.volume = volume
  notify()
}

export function addToQueue(index) {
  if (index < 0 || index >= playlist.length || index === currentTrack) return
  if (!queue.includes(index)) queue.push(index)
  notify()
}

export function removeFromQueue(index) {
  queue = queue.filter(i => i !== index)
  notify()
}

export function clearQueue() {
  queue = []
  notify()
}

audio.addEventListener('timeupdate', notify)
audio.addEventListener('ended', nextTrack)
audio.addEventListener('loadedmetadata', notify)

export function subscribe(fn) {
  listeners.push(fn)
  const track = playlist[currentTrack]
  fn({
    currentTrack,
    isPlaying,
    volume,
    progress: track ? audio.currentTime / track.duration : 0,
    track,
    queue,
    currentTime: audio.currentTime,
  })
  return () => { listeners = listeners.filter(l => l !== fn) }
}
